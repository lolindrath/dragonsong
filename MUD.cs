using System;
using System.Collections;
using System.IO;

namespace Dragonsong
{
	/// <summary>
	/// This represents an instance
	/// of a MUD
	/// </summary>
	public class Mud
	{
		private string name;
		private string host;
		private int port;
		private string desc;
		private string loginScript;

		/// <summary>
		/// Creates a new <see cref="Mud"/> instance.
		/// </summary>
		/// <param name="n">N.</param>
		public Mud(string n)
		{
			name = n;
		}

		/// <summary>
		/// Gets or sets the name.
		/// </summary>
		/// <value></value>
		public string Name
		{
			get { return name; }
			set { name = value; }
		}

		/// <summary>
		/// Gets or sets the host.
		/// </summary>
		/// <value></value>
		public string Host
		{
			get { return host; }
			set { host = value; }
		}

		/// <summary>
		/// Gets or sets the port.
		/// </summary>
		/// <value></value>
		public int Port
		{
			get { return port; }
			set { port = value; }
		}

		/// <summary>
		/// Gets or sets the description.
		/// </summary>
		/// <value></value>
		public string Description
		{
			get { return desc; }
			set { desc = value; }
		}

		/// <summary>
		/// Gets or sets the login script.
		/// </summary>
		/// <value></value>
		public string LoginScript
		{
			get { return loginScript; }
			set { loginScript = value; }
		}
	}
}
